## Calcular el perimetro y área de un rectángulo dada su base y su altura

base = float(input("Ingrese la medida de la base"))

altura = float (input("Ingrese la medida de la altura"))

lateral = float (input("Ingrese la medida del lateral"))

area = (base * altura ) / 2

perimetro = 3 * lateral

print ("¿Aqui tienes el área ?" , area )

print ("¿La medida del perimetro es :?" , perimetro)

