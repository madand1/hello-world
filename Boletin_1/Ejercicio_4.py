## Dados dos numeros, mostrar la suma, resta, division y multiplicacion de ambos

numero1 = float (input("Dame el primer numero"))

numero2 = float (input("Dame el segundo numero"))

suma  = numero1 + numero2

resta = numero1 - numero2
print ("Aqui tienes la suma de ambos numeros" , suma)

print ("Aquie tienes la resta de ambos numeros " ,resta)

division = numero1 / numero2

print ("Aqui tienes la division de ambos numeros ", division)

multiplicacion = numero1 * numero2

print ("Aqui tienes la multiplicacion de ambos numeros " , multiplicacion)

